func GetTitle(width int) (Title, int) {
	for _, t := range titles {
		lines := strings.Split(strings.Trim(t.ASCII, "\n"), "\n")
		artWidth := maxLineLength(lines)
		if artWidth <= width {
			return t, artWidth
		}
	}

	defaultTitle := titles[4]
	defaultMaxLineLength := maxLineLength(strings.Split(strings.Trim(defaultTitle.ASCII, "\n"), "\n"))

	return defaultTitle, defaultMaxLineLength

}

func maxLineLength(lines []string) int {
	max := 0
	for _, l := range lines {
		if len(l) > max {
			max = len(l)
		}
	}
	return max
}
