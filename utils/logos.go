var Logos = struct {
	Debian  []string
	Ubuntu  []string
	Windows []string
	Default []string
}{
	Debian:  debianAscii,
	Ubuntu:  ubuntuAscii,
	Windows: windowsAscii,
	Default: defaultLogoAscii,
}

func GetLogoByOS(os string) []string {
	switch os {
	case "Debian":
		return Logos.Debian
	case "Ubuntu":
		return Logos.Ubuntu
	case "Windows":
		return Logos.Windows
	default:
		return Logos.Default
	}
}

